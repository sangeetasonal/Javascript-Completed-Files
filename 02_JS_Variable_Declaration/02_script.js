// let -> key word to declare any variable
// a -> variable name / reference variable
// = -> assignment operator
// 10 -> value / data
// ; -> end of statement

let a = 10;
let b = 20;
let sum = a + b;
console.log('The sum of ' + a  + ' , ' +  b + ' is : ' + sum); // ES5
console.log(`The sum of ${a} , ${b} is : ${sum}`); // ES6

// Rules of Declaring variables
// 1) all variables are case sensitive
// 2) re-declaration of same variable is not allowed
// 3) camel-case in variable declaration
// 4) max 15 chars allowed
// 5) allowed characters : a - z , A - Z , _ , $ , 0 - 9
// 6) must not starts with the number

