// String
let empName = 'John';
console.log(`Value : ${empName} Type : ${typeof empName}`);

// Number
let empSalary = 85000;
console.log(`Value : ${empSalary} Type : ${typeof empSalary}`);

// Boolean
let isManager = true;
console.log(`Value : ${isManager} Type : ${typeof isManager}`);

// undefined-- if the object is present but not assigned.
let isMarried;
console.log(`Value : ${isMarried} Type : ${typeof isMarried}`);

// null ---dummy object not in use
isMarried = null;
console.log(`Value : ${isMarried} Type : ${typeof isMarried}`);

// Re-Assignment
let test;
console.log(`Value : ${test} Type : ${typeof test}`);

test = 'abc';
console.log(`Value : ${test} Type : ${typeof test}`);

test = true;
console.log(`Value : ${test} Type : ${typeof test}`);

test = null;
console.log(`Value : ${test} Type : ${typeof test}`);